int main ()
{
    int A , B , X;
    string s ;

    if (s == "End Of File")
    {
        return 0 ;
    }
    else
    {
        while (s != "End Of File" )
        {
            cin >> A >> B ;

            X = B - A ;
        }
        cout << X << endl;
    }

}
